int	ft_sqrt(int nb)
{
	long	n;
	long	lnb;

	lnb = nb;
	n = 0;
	if (nb < 0)
		return (0);
	while (n * n <= lnb)
	{
		if (n * n == lnb)
			return (n);
		n++;
	}
	return (0);
}
